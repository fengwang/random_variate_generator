static std::size_t rand_gen( const std::size_t lower, const std::size_t upper )
{
    static unsigned int seed = time( 0 );
    srand( seed );
    seed += rand();
    std::size_t ans( rand() );
    ans %= ( upper - lower );
    ans += lower;
    return ans;
}

std::string r_gen( const std::string* cache, const std::size_t length )
{
    const std::size_t r = rand_gen( 0, length );
    return cache[r];
}

const std::size_t w[] = { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2, 1 };

const std::size_t generate_18( const std::string& str )
{
    const std::size_t ans = std::inner_product( w, w + 17, &str[0], std::size_t( 0 ),
                                                []( std::size_t s, const std::size_t t ) -> std::size_t {return s + t;},
                                                []( std::size_t s, char ch ) -> std::size_t {std::size_t t = ch - '0'; return s* t; }
                                              );
    return ans;
}

void generate_id()
{
    const std::string p = r_gen( pos, sizeof( pos ) / sizeof( 0[pos] ) );
    const std::string y = r_gen( year, sizeof( year ) / sizeof( 0[year] ) );
    const std::string m = r_gen( month, sizeof( month ) / sizeof( 0[month] ) );
    const std::string d = r_gen( day, sizeof( day ) / sizeof( 0[day] ) );
    const std::size_t o_ = rand_gen( 1, 999 );
    assert( 6 == p.size() );
    assert( 4 == y.size() );
    assert( 2 == m.size() );
    assert( 2 == d.size() );
    std::stringstream ss;
    ss << o_;
    std::string o;
    ss >> o;

    if ( o_ < 100 )
        { o = std::string( "0" ) + o; }

    if ( o_ < 10 )
        { o = std::string( "0" ) + o; }

    const std::string i17 = p + y + m + d + o;

    std::size_t i18 = generate_18( i17 );

    i18 %= 11;

    i18 = 12 - i18;

    i18 %= 11;

    std::cout << i17;

    if ( i18 < 10 )
        { std::cout << i18; }
    else
        { std::cout << 'X'; }

    std::cout << std::endl;
}

